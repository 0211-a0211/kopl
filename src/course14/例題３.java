package course14;

// 整数リテラル（10進／8進／16進）

class 例題３ {

	public static void main(String[] args) {
		int x = 100; // 10進数の13
		int y = 0b1100100; //  8進数の13
		int z = 0x64; // 16進数の13

		System.out.println("x = " + x);
		System.out.println("y = " + y);
		System.out.println("z = " + z);
	}
}
